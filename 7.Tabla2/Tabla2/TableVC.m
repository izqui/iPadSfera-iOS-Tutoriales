-(void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender{
    
    if ([segue.identifier isEqualToString:@"detail"]){
        
        NSString *elemento = [array objectAtIndex:[[self.tableView indexPathForSelectedRow] row]];
        [segue.destinationViewController setElemento:elemento];
    }
}
